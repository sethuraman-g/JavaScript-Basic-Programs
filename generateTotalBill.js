function getTotalAmount(storeList,userList){

   let totalAmount = 0; let currentQuantity = 0;

    storeList.forEach(productDetail => {

        userList.forEach(userDetail => {

            if(productDetail.product === userDetail.item && 
                (typeof (productDetail.product) === "string" && 
                 typeof (userDetail.item) === "string")) {

               if(userDetail.quantity > productDetail.Quantity){ 
                currentQuantity = productDetail.Quantity;//multiply with store quantity alone
                 //didn't consider the already existing item
                productDetail.Quantity -= userDetail.quantity;    
               }
               else{
                currentQuantity = userDetail.quantity;
                //didn't consider the  already existing item
                productDetail.Quantity -= userDetail.quantity;     

               }
               // removing special characters and converting the string to number
               let price = productDetail.price.replace(/[$]/g,"");  
               totalAmount += price * currentQuantity;
            }
            else
            {
                return `Invalid Inputs`;
            }
        })
    })
    return `$ ${totalAmount.toFixed(2)}`;
}

